CREATE TABLE `scan_schedules` (
  `ACTIVE` varchar(10) DEFAULT NULL,
  `REF` varchar(10) DEFAULT NULL,
  `TITLE` varchar(255) DEFAULT NULL,
  `TARGETS` text,
  `START_DATE_UTC` datetime DEFAULT NULL,
  `START_HOUR` int(1) DEFAULT NULL,
  `START_MINUTE` int(1) DEFAULT NULL,
  `END_AFTER_HOURS` int(1) DEFAULT NULL,
  `TIME_ZONE_CODE` varchar(50) DEFAULT NULL,
  `TIME_ZONE_DETAILS` varchar(255) DEFAULT NULL,
  `DST_SELECTED` int(1) DEFAULT NULL,
  `RECURRENCE` varchar(20) DEFAULT NULL,
  `DEFAULT_SCANNER` int(1) DEFAULT NULL,
  `NEXTLAUNCH_UTC` datetime DEFAULT NULL,
  `ISCANNER_NAME` varchar(255) DEFAULT NULL,
  `OPTION` varchar(255) DEFAULT NULL,
  `TYPE` varchar(50) DEFAULT NULL,
  `OPTION_PROFILE` varchar(255) DEFAULT NULL,
  `LAST_UPDATED` datetime DEFAULT NULL,
  `ACCOUNT` varchar(50) DEFAULT NULL,
  KEY `ACTIVE` (`ACTIVE`),
  KEY `TYPE` (`TYPE`),
  KEY `ACCOUNT` (`ACCOUNT`),
  KEY `NEXTLAUNCH` (`NEXTLAUNCH_UTC`)
) ENGINE=MyISAM DEFAULT CHARSET=latin1;
